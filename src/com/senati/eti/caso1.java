package com.senati.eti;

public class caso1 {

	public static void main(String[] args) {
		// Estructuras repetitivas
		//mientras - while
		//para - for
		// si quieres dos seria x + =2
		System.out.println("WHILE- contador ascendente");
		System.out.println("--------------------------");
		int x = 1;
		while (x <=5){
			System.out.println("Bienvenido " + x );
			x++;
			
		}
		System.out.println("WHILE- contador descendente");
		System.out.println("---------------------------");
		int c = 5;
		
		while (c >=1 ) {
			System.out.println("Bienvenido "+ c);
			c--;
		}

	}

}
